package com;

public class User {
	private int id;
	private String name;
	private String hash_pass;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHash_pass() {
		return hash_pass;
	}
	public void setHash_pass(String hash_pass) {
		this.hash_pass = hash_pass;
	}
}
